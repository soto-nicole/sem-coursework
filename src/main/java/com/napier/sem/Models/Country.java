package com.napier.sem.Models;

/**
 * A class that creates Country objects
 */
public class Country
{
    /**
     * The country code - a unique abbreviated form of identification for the country
     */
    public String code;
    /**
     * The name of the country
     */
    public String name;
    /**
     * The continent within which the country is located
     */
    public String continent;
    /**
     * The region within which the country is located
     */
    public String region;
    /**
     * The total number of people who live in the country
     */
    public int population;
    /**
     * The name of the country's capital city
     */
    public String capitalName;
}
